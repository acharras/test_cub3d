#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	long	res;
	int		m;

	i = 0;
	res = 0;
	m = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		m = 1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (m == 0)
		return (res);
	else
		return (-res);
}
