#include "libft.h"

void	ft_putnbr(int n)
{
	long	i;

	i = n;
	if (i < 0)
	{
		i = i * -1;
		ft_putchar('-');
	}
	if (i >= 10)
		ft_putnbr(i / 10);
	ft_putchar('0' + (i % 10));
}
