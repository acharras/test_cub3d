#include "libft.h"

int		ft_int_len(int nbr)
{
	int		len;

	len = 0;
	if (nbr < 0)
		len++;
	while (nbr / 10 >= 1 || nbr / 10 <= -1)
	{
		nbr /= 10;
		len += 1;
	}
	len++;
	return (len);
}
